const solution = (chicken) => {
  let coupon = chicken;
  let service = 0;
  let result = 0;
  let rem = 0;

  while (coupon >= 10) {
    service = Math.floor(coupon / 10);
    result += service;
    rem += coupon % 10;
    coupon = service;
  }

  if (rem + coupon > 10) {
    result += Math.floor((rem + coupon) / 10);
    const NewRem = (rem + coupon) % 10;
    coupon = Math.floor((rem + coupon) / 10);
    rem = NewRem;
  }

  rem = Math.floor((rem + coupon) / 10);

  return result + rem;
};

