const solution = (budget, arr) => {
  let answer = 0;
  let n = arr.length; // í•™ìƒ ìˆ˜

  // ì´ ë¹„ìš©ìœ¼ë¡œ ì˜¤ë¦„ì°¨ìˆœ ì •ë ¬
  arr.sort((a, b) => a[0] + a[1] - (b[0] + b[1]));

  // ëª¨ë“  ë¬¼í’ˆì— ëŒ€í•´ í•´ë‹¹ ë¬¼í’ˆì´ ë°˜ê°’ì¼ ë•Œì˜ ê²½ìš°ì˜ ìˆ˜ í™•ì¸í•˜ê¸°
  for (let i = 0; i < n; i++) {
    let red = budget - (arr[i][0] / 2 + arr[i][1]);
    let cnt = 1;
    // ë°˜ê°’ í• ì¸ëœ ë¬¼í’ˆ ì œì™¸í•˜ê³  ìµœì†Œ ë¹„ìš©ë¶€í„° ì­‰ ì‚¬ê¸°
    for (let j = 0; j < n; j++) {
      const price = arr[j][0] + arr[j][1];
      if (j !== i) {
        if (price > red) break;
        red -= price;
        cnt++;
      }
    }
    answer = Math.max(answer, cnt);
  }

  return answer;
};

